package com.chw.ssm.po;

/*
 * 用来存放每一科目的成绩
 */
public class Report {

	private String name;
	
	private String grade;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getGrade() {
		return grade;
	}

	public void setGrade(String grade) {
		this.grade = grade;
	}
	
	
	
}
